package de.clusterfreak.ClusterCore;

/**
 * Point with pixel coordinates
 *
 * @version 1.0.3 (2021-05-13)
 * @author Thomas Heym
 */
public class PointPixel {
    /**
     * x
     */
    public int x = 0;
    /**
     * y
     */
    public int y = 0;

    /**
     * Set PointPixel
     * @param xi x
     * @param yi y
     */
    public PointPixel(int xi, int yi) {
        x = xi;
        y = yi;
    }
}
